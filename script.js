// Service card interactions
document.addEventListener('DOMContentLoaded', () => {
    const serviceCards = document.querySelectorAll('.service-card');
    
    serviceCards.forEach(card => {
        card.addEventListener('click', () => {
            // Scroll to contact section when service card is clicked
            const contactSection = document.querySelector('#contact');
            if (contactSection) {
                contactSection.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
                
                // Pre-fill service type in form
                const serviceSelect = document.querySelector('#service');
                const cardTitle = card.querySelector('h3').textContent.toLowerCase();
                
                if (serviceSelect) {
                    if (cardTitle.includes('buying')) {
                        serviceSelect.value = 'buying';
                    } else if (cardTitle.includes('selling')) {
                        serviceSelect.value = 'selling';
                    } else if (cardTitle.includes('rental')) {
                        serviceSelect.value = 'renting';
                    }
                }
            }
        });
    });
});

